/**
 * Attempts to extract a title / objective from the prompt text.
 * Looks for common patterns like "title:", "objective:", or the first line.
 * @param {string} prompt
 * @returns {string}
 */
function extractTitle(prompt) {
  const titleMatch = prompt.match(/(?:title|name)\s*[:\-]\s*(.+)/i);
  if (titleMatch) return titleMatch[1].trim();

  const objectiveMatch = prompt.match(/(?:objective|goal|about)\s*[:\-]\s*(.+)/i);
  if (objectiveMatch) return objectiveMatch[1].trim();

  // Fall back to first non-empty line, capped at 80 chars
  const firstLine = prompt.split('\n').find(l => l.trim().length > 0) || '';
  return firstLine.slice(0, 80).trim() || 'Untitled';
}


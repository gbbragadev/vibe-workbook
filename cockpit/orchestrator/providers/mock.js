// ---------------------------------------------------------------------------

class MockProvider extends BaseProvider {
  get name() {
    return 'mock';
  }

  isAvailable() {
    return true;
  }

  async generate(lane, prompt, opts = {}) {
    const start = Date.now();

    logger.lane('mock', 'Generating content for lane "' + lane + '"');

    let content;
    switch (lane) {
      case 'architecture':
        content = generateArchitecture(prompt);
        break;
      case 'planning':
        content = generatePlanning(prompt);
        break;
      case 'research':
        content = generateResearch(prompt);
        break;
      case 'build':
        content = generateBuild();
        break;
      case 'qa':
        content = generateQA(prompt);
        break;
      case 'evaluation':
        content = generateEvaluation(prompt);
        break;
      default:
        content = 'Content generated for lane: ' + lane + '\n\n' + prompt.slice(0, 200);
    }

    const duration = Date.now() - start;
    logger.done('MockProvider finished in ' + duration + 'ms');

    return { content, provider: this.name, duration };
  }
}

module.exports = { MockProvider };
